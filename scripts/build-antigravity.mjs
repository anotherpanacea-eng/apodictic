#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");
const checkOnly = process.argv.includes("--check");

const registryPath = path.join(repoRoot, "release-registry.json");
if (!fs.existsSync(registryPath)) {
  console.error(`Missing registry: ${registryPath}`);
  process.exit(1);
}

const registry = JSON.parse(fs.readFileSync(registryPath, "utf8"));
const { paths = {}, commands = [], antigravity } = registry;

if (!antigravity) {
  console.error("Missing antigravity section in release-registry.json.");
  process.exit(1);
}

function abs(relPath) {
  return path.resolve(repoRoot, relPath);
}

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function mustExist(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing file: ${path.relative(repoRoot, filePath)}`);
  }
}

function mustRead(filePath) {
  mustExist(filePath);
  return fs.readFileSync(filePath, "utf8");
}

function readJson(filePath) {
  return JSON.parse(mustRead(filePath));
}

function writeFile(filePath, content) {
  ensureDir(path.dirname(filePath));
  fs.writeFileSync(filePath, content, "utf8");
}

function removePath(targetPath) {
  fs.rmSync(targetPath, { recursive: true, force: true });
}

function walkFiles(dirPath) {
  const files = [];
  if (!fs.existsSync(dirPath)) return files;

  for (const entry of fs.readdirSync(dirPath, { withFileTypes: true })) {
    const fullPath = path.join(dirPath, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }

  return files.sort();
}

function compareTrees(expectedDir, actualDir) {
  const expectedFiles = walkFiles(expectedDir);
  const actualFiles = walkFiles(actualDir);
  const expectedMap = new Map(expectedFiles.map((file) => [path.relative(expectedDir, file), file]));
  const actualMap = new Map(actualFiles.map((file) => [path.relative(actualDir, file), file]));
  const diffs = [];

  for (const relPath of [...expectedMap.keys()].sort()) {
    if (!actualMap.has(relPath)) {
      diffs.push(`Missing generated file: ${relPath}`);
      continue;
    }

    const expectedBuffer = fs.readFileSync(expectedMap.get(relPath));
    const actualBuffer = fs.readFileSync(actualMap.get(relPath));
    if (!expectedBuffer.equals(actualBuffer)) {
      diffs.push(`Generated file differs: ${relPath}`);
    }
  }

  for (const relPath of [...actualMap.keys()].sort()) {
    if (!expectedMap.has(relPath)) {
      diffs.push(`Unexpected generated file: ${relPath}`);
    }
  }

  return diffs;
}

function extractFrontmatterVersion(filePath) {
  const match = mustRead(filePath).match(/^version:\s*(.+)$/m);
  return match ? match[1].trim() : null;
}

function buildAntigravityWorkspaceReadme() {
  return `# Antigravity APODICTIC Workspace

<!-- Generated by scripts/build-antigravity.mjs. Do not edit files in antigravity/ by hand. -->

This workspace is generated from the canonical APODICTIC source in this repo for native Antigravity execution.

## Layout

- \`plugins/apodictic/\` — generated plugin workspace
- \`.agents/workflows/\` — native command entrypoints
`;
}

function buildNativeWorkflow(commandMeta) {
  const slug = commandMeta.command.replace(/^\//, "");
  
  return `---
description: ${commandMeta.description || "APODICTIC native command"}
---

<!-- Generated by scripts/build-antigravity.mjs. Do not edit. -->
# ${commandMeta.command}

This command loads the canonical APODICTIC ${commandMeta.command} workflow.

## Execution Rules
1. DO NOT duplicate state-gardening or resume mechanics here.
2. Read the canonical command doc: \`plugins/apodictic/commands/${slug}.md\`.
3. Follow all routing, resume gate (if applicable), and execution mode semantics specified there exactly.
4. Explicitly invoke the \`@core-orchestrator\` subagent to execute the passes required by the command, using the canonical execution model as instructed by the canonical doc.
5. Follow the folder architecture: rolling state at the project root, run artifacts in \`runs/\`. See \`plugins/apodictic/skills/core-editor/references/output-policy.md\` §Folder Architecture.
`;
}

function stripHostSpecificFiles(pluginDir) {
  for (const filePath of walkFiles(pluginDir)) {
    const baseName = path.basename(filePath);
    if (
      baseName === ".DS_Store" ||
      baseName.includes(".codex.") ||
      baseName.includes(".claude-plugin") ||
      filePath.includes("/.codex-plugin") ||
      filePath.includes("/.claude-plugin")
    ) {
      fs.rmSync(filePath, { force: true, recursive: true });
    }
  }

  const codexDir = path.join(pluginDir, ".codex-plugin");
  if (fs.existsSync(codexDir)) removePath(codexDir);
  
  const claudeDir = path.join(pluginDir, ".claude-plugin");
  if (fs.existsSync(claudeDir)) removePath(claudeDir);
}

function buildPluginJson(canonicalManifest, version) {
  return {
    name: canonicalManifest.name,
    version: version,
    description: canonicalManifest.description,
    author: canonicalManifest.author,
    keywords: canonicalManifest.keywords,
    interface: {
      displayName: antigravity.displayName,
      category: antigravity.category,
      defaultPrompt: antigravity.defaultPrompts
    }
  };
}

function buildSubagent(tempPluginDir) {
  const orchestratorDir = path.join(tempPluginDir, "agents", "core-orchestrator");
  ensureDir(orchestratorDir);
  
  writeFile(
    path.join(orchestratorDir, "agent.json"),
    JSON.stringify({
      name: "core-orchestrator",
      description: "Thin Antigravity agent orchestrator handling APODICTIC execution modes.",
      tools: ["read_file", "write_to_file", "search_web", "run_command"]
    }, null, 2)
  );

  writeFile(
    path.join(orchestratorDir, "instructions.md"),
    `You are the APODICTIC Core Orchestrator Subagent.

Your role is to act as a thin dispatcher executing APODICTIC's canonical lifecycle.

## Execution Rules
1. Load \`plugins/apodictic/skills/core-editor/references/run-core.md\` and \`state-lifecycle.md\`.
2. Follow canonical execution mode logic exactly as described in the canonical references:
   - Choose execution mode (e.g., \`single-agent\`, \`sequential\`) based on context-window and manuscript-load checks.
   - If user explicitly overrides, respect \`hybrid\` or \`swarm\`.
3. Handle state gardening autonomously if state lines > 500, and consult the user if 300-500.
4. Do NOT invent new pass lifecycles.
5. All diagnosis, mechanism, and intervention must strictly respect the APODICTIC firewall rules.

## Folder Architecture
Follow \`plugins/apodictic/skills/core-editor/references/output-policy.md\` §Folder Architecture:
- Rolling state (\`Diagnostic_State.md\`, \`SYNTHESIS.md\`, \`README.md\`) lives at the **project root**.
- Run artifacts (pass reports, contracts, findings ledgers, audit reports) go into \`runs/YYYY-MM-DD_{model}_{type}/\` inside the project root.
- Create the run folder at the start of each run; update rolling state at the project root after synthesis.
- Never write project state into the plugin directory.`
  );
}



function main() {
  const canonicalManifestPath = abs(paths.rootPluginJson);
  const canonicalManifest = readJson(canonicalManifestPath);
  const canonicalVersion = canonicalManifest.version;
  
  const workspaceDir = abs(antigravity.workspaceDir);
  const tempRoot = fs.mkdtempSync(path.join(repoRoot, ".antigravity-build-tmp-"));
  const tempWorkspace = path.join(tempRoot, "antigravity");
  const tempPluginDir = path.join(tempWorkspace, "plugins", "apodictic");

  try {
    ensureDir(path.join(tempWorkspace, "plugins"));
    
    // Copy canonical plugin
    fs.cpSync(abs("plugins/apodictic"), tempPluginDir, { recursive: true });

    // Strip host specific files
    stripHostSpecificFiles(tempPluginDir);

    // Apply Overrides (if any)
    for (const [sourceRelPath, destRelPath] of Object.entries(antigravity.overrides || {})) {
      const sourcePath = abs(sourceRelPath);
      mustExist(sourcePath);
      const destPath = path.join(tempWorkspace, destRelPath);
      ensureDir(path.dirname(destPath));
      fs.copyFileSync(sourcePath, destPath);
    }

    // Write plugin.json
    writeFile(
      path.join(tempPluginDir, "plugin.json"),
      JSON.stringify(buildPluginJson(canonicalManifest, canonicalVersion), null, 2) + "\\n"
    );

    // Build Orchestrator Tooling
    buildSubagent(tempPluginDir);

    // Write Native Workflows
    for (const commandMeta of commands) {
      const slug = commandMeta.command.replace(/^\//, "");
      writeFile(
        path.join(tempWorkspace, ".agents", "workflows", `${slug}.md`),
        buildNativeWorkflow(commandMeta)
      );
    }

    // Write Workspace README and Parity Notes
    writeFile(path.join(tempWorkspace, "README.md"), buildAntigravityWorkspaceReadme());
    
    const parityNotes = `# Antigravity Parity Notes

This file tracks the architectural alignment between the original APODICTIC framework and the native Antigravity packaging.

## Current Alignments
1. **Slash Command Routing**: Native Antigravity \`.agents/workflows/\` intercept commands (e.g. \`/start\`, \`/develop-edit\`) directly from the user chat.
2. **Execute Delegation**: Antigravity's \`core-orchestrator\` Subagent autonomously reads state triggers and executes the canonical \`run-core.md\` execution logic without requiring proxy wrappers.
3. **Canonical Truth**: All files strictly reference the single authoritative APODICTIC runtime.
4. **Folder Architecture**: Output follows the v0.5.0 folder architecture — rolling state at the project root, run artifacts in \`runs/YYYY-MM-DD_{model}_{type}/\`. See \`output-policy.md\` §Folder Architecture.
`;
    writeFile(path.join(tempWorkspace, "NON_PARITY_NOTES.md"), parityNotes);

    if (checkOnly) {
      if (!fs.existsSync(workspaceDir)) {
        throw new Error("Generated antigravity workspace is missing. Run scripts/build-antigravity.mjs.");
      }

      const diffs = compareTrees(tempWorkspace, workspaceDir);
      if (diffs.length > 0) {
        const preview = diffs.slice(0, 40).map((diff) => `- ${diff}`).join("\n");
        const remainder = diffs.length > 40 ? `\n- ... (${diffs.length - 40} more)` : "";
        throw new Error(`Generated antigravity workspace is stale.\n${preview}${remainder}`);
      }

      console.log("build-antigravity check passed.");
      return;
    }

    removePath(workspaceDir);
    fs.renameSync(tempWorkspace, workspaceDir);
    console.log(`Generated workspace: ${path.relative(repoRoot, workspaceDir)}`);

  } finally {
    removePath(tempRoot);
  }
}

try {
  main();
} catch (error) {
  console.error(`build-antigravity failed: ${error.message}`);
  process.exit(1);
}
